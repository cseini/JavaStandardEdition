public class Calc01{
	public static void main(String[] args){
		int num1 = 10;
		int num2 = 3;
		int num3 = num1 + num2;
		int num4 = num1 - num2;
		int num5 = num1 * num2;
		int num6 = num1 / num2;
		int num7 = num1 % num2;

			System.out.println("num1과 num2의 합 : "+num3);
			System.out.println("num1과 num2의 차 : "+num4);
			System.out.println("num1과 num2의 곱 : "+num5);
			System.out.println("num1과 num2의 나눗셈 : "+num6);
			System.out.println("num1과 num2의 나머지 : "+num7);

	}
}